from math import radians, sin, cos, sqrt, atan2


def haversine(lat1, lon1, lat2, lon2):
    """Return distance in miles between two lat/lon points."""
    R = 3958.8  # Earth radius in miles
    lat1, lon1, lat2, lon2 = map(radians, [lat1, lon1, lat2, lon2])
    dlat = lat2 - lat1
    dlon = lon2 - lon1
    a = sin(dlat/2)**2 + cos(lat1) * cos(lat2) * sin(dlon/2)**2
    c = 2 * atan2(sqrt(a), sqrt(1 - a))
    return R * c


def get_school(school_id):
    """Return school dict by ID, or None if not found."""
    return SCHOOLS_DATABASE.get(int(school_id))


def get_schools_by_division(division):
    """Return list of all school dicts for a given division (D1, D2, JUCO)."""
    return [s for s in SCHOOLS_DATABASE.values() if s["division"] == division]


def distance_between_schools(id1, id2):
    """Return distance in miles between two schools by ID."""
    s1 = get_school(id1)
    s2 = get_school(id2)
    if not s1 or not s2:
        return None
    return haversine(s1["latitude"], s1["longitude"], s2["latitude"], s2["longitude"])


