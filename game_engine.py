def _select_shot_bucket(possession_state, shooter, offense_lineup,
                        defense_lineup, coach_profile, preferred_bucket=None):
    """
    Selects shot bucket given possession state and player tendencies.
    Returns bucket name string.

    preferred_bucket overrides selection (e.g., drive forced at_rim).
    """
    if preferred_bucket and preferred_bucket in SHOT_BUCKETS:
        return preferred_bucket

    # Start with possession state base weights
    base = BASE_BUCKET_WEIGHTS.get(possession_state,
                                    BASE_BUCKET_WEIGHTS["half_court"])
    weights = list(base)  # copy

    # Coach philosophy adjustments
    shot_profile = coach_profile.get("shot_profile",  50)  # high = perimeter/three
    inside_out   = coach_profile.get("inside_out_outside_in", 50)

    # High shot_profile pushes toward threes
    if shot_profile > 55:
        bump = (shot_profile - 55) * 0.3
        weights[4] = max(2, weights[4] + bump)   # corner_three
        weights[5] = max(2, weights[5] + bump)   # above_break_three
        weights[3] = max(2, weights[3] - bump * 0.5)  # mid_two

    # Inside-out shifts post vs perimeter
    if inside_out < 40:
        weights[2] = max(2, weights[2] + (40 - inside_out) * 0.3)  # post
    elif inside_out > 60:
        weights[4] = max(2, weights[4] + (inside_out - 60) * 0.2)  # corner_three

    # Player attribute adjustments
    # A three-point specialist naturally gravitates toward three buckets
    three = shooter.get("three_point", 400)
    post  = shooter.get("post_scoring", 400)
    fin   = shooter.get("finishing",    400)

    if three > 600:
        weights[4] = max(2, weights[4] * (1 + (three - 600) / 1000.0))
        weights[5] = max(2, weights[5] * (1 + (three - 600) / 1000.0))
    if post > 600:
        weights[2] = max(2, weights[2] * (1 + (post - 600) / 800.0))
    if fin > 650:
        weights[0] = max(2, weights[0] * (1 + (fin - 650) / 800.0))

    # Putback always 0 in normal selection -- only via offensive rebound
    weights[6] = 0

    bucket = random.choices(BUCKET_ORDER, weights=weights, k=1)[0]
    return bucket


