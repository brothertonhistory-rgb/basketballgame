# -----------------------------------------
# COLLEGE HOOPS SIM -- Game Engine v1.0  (Phase B)
# System 1 of the Design Bible
#
# PHASE B CHANGES -- Complete engine refactor.
#
#   SHOT BUCKET MODEL (C1):
#     Every FGA is one of 7 explicit buckets:
#       at_rim, floater, post, mid_two,
#       corner_three, above_break_three, putback
#     catch_and_shoot is now a SKILL modifier on three buckets.
#     It is NOT a shot type. Threes come from three-bucket selection.
#     No coin flips. No blurring.
#
#   POSSESSION STATES (C2):
#     1. half_court   -- default, no advantage
#     2. drive        -- guard beat his man
#     3. post_up      -- big established position
#     4. ball_movement -- defense rotated, open look
#     5. transition   -- fastbreak
#
#   DRIVE/ADVANTAGE MODEL (C3):
#     drive_success uses explosiveness, speed, ball_handling
#     vs defender lateral_quickness, explosiveness, on_ball_defense.
#     Successful drive shifts buckets toward at_rim/floater.
#     Kick-out decision fires if rim protector present.
#
#   PHYSICAL MATCHUP INTEGRATION (C4):
#     length_rating, frame_strength, athleticism_composite
#     calculated at matchup time (never stored on player).
#     Height/wingspan affect contests. Frame strength drives post battles.
#
#   FOUL MODEL (C7):
#     Fouls generated by shot context, not generically.
#     at_rim drives: blocking foul from agility/positioning
#     post battles: reach-in from strength disparity
#     perimeter: hand-check from length/aggression
#     Foul-outs enforced. Foul trouble reduces minutes.
#
#   ASSIST MODEL (C8):
#     Possession state determines assist eligibility.
#     Ball movement and kick-outs credit the passer.
#     Self-created shots get no assist.
#     AST/FGM is emergent, not tuned.
#
#   TURNOVER BUCKETS (C5):
#     bad_pass, ball_handling, charge, shot_clock, intercepted
#     Each has its own steal attribution logic.
#
#   BURST SYSTEM (C9):
#     No rubber band logic. Behavior inputs change, not make rates.
#     Composure run / collapse / lock_in / complacency.
#     Timeout mechanic with finite resource tracking.
#
#   CALIBRATION TARGETS (F1) -- 2009-10 D1 averages:
#     Points/team/game:  68-72
#     3PA/FGA:           33-36%
#     FTA/FGA:           38-42%
#     TO%/possession:    19-21%
#     Block% of 2PA:     8-10%
#     AST/FGM:           52-56%
#     Fouls/team/game:   18-22
# -----------------------------------------


