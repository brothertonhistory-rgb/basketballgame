def _length_rating(player):
    """Wingspan and height combined length advantage."""
    ws = player.get("wingspan", 74)
    h  = player.get("height",   74)
    return (ws - 72) * 15 + (h - 72) * 10


def _frame_strength(player):
    """Physical force. Post dominance, boxing out."""
    w  = player.get("weight",   200)
    st = player.get("strength", 400)
    return (w / 220.0) * (st / 500.0)


def _athleticism_composite(player):
    """Defensive mobility, drive success, transition."""
    sp  = player.get("speed",            400)
    lq  = player.get("lateral_quickness",400)
    vt  = player.get("vertical",         400)
    ex  = player.get("explosiveness",    400)
    return sp * 0.25 + lq * 0.25 + vt * 0.20 + ex * 0.30


