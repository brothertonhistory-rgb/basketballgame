# TECHNICAL FOULS, FLAGRANT FOULS, ILLEGAL SCREENS
# -----------------------------------------

# Per-possession base probabilities (tuned for realistic frequency)
# Real D1: ~0.5-1.0 technicals per game total, ~0.1-0.2 flagrants
TECH_CLASS_B_BASE    = 0.004   # ~0.3/game: delay, rim hang, admin
TECH_CLASS_A_BASE    = 0.003   # ~0.2/game: arguing, taunting, obscenities
FLAGRANT_1_BASE      = 0.002   # ~0.15/game: excessive contact
FLAGRANT_2_BASE      = 0.0003  # ~0.02/game: intentional/severe -- very rare
ILLEGAL_SCREEN_BASE  = 0.012   # ~0.8/game: moving screens, illegal contact


def _check_technical_fouls(offense_lineup, defense_lineup,
                             offense_team, defense_team,
                             game_state, offense_label, burst_modifier,
                             score_diff, possession_num, total_possessions):
    """
    Checks for technical fouls (Class A and B), flagrant fouls,
    and illegal screens on this possession.

    Returns a result dict if a technical/flagrant/illegal screen fires,
    or None if nothing happened.

    Result dict format matches simulate_possession return format.
    """
    defense_label = "away" if offense_label == "home" else "home"
    progress = possession_num / max(1, total_possessions)

    # --- ILLEGAL SCREEN CHECK (offense commits) ---
    # Screener has low agility OR coach runs heavy screen-heavy system
    # Results in offensive foul -- turnover, no FTs
    screen_prob = ILLEGAL_SCREEN_BASE
    # Heavy screening systems get called more
    off_coach = offense_team.get("coach", {}) if offense_team else {}
    ball_mov  = off_coach.get("ball_movement", 50)
    if ball_mov > 65:
        screen_prob += (ball_mov - 65) / 100.0 * 0.008

    if random.random() < screen_prob and offense_lineup:
        # Pick screener: PF/C with low agility most likely
        screener_scores = []
        for p in offense_lineup:
            pos_weight = {"PF": 2.5, "C": 2.5, "SF": 1.5, "SG": 0.8, "PG": 0.6}.get(
                p.get("position", "SF"), 1.0)
            agility_penalty = max(0.5, 1.5 - p.get("agility", 400) / 600.0)
            screener_scores.append(pos_weight * agility_penalty)

        screener = random.choices(offense_lineup, weights=screener_scores, k=1)[0]
        screener["foul_count"] = screener.get("foul_count", 0) + 1
        accumulate_game_stats(screener, "fouls")

        return {
            "outcome":    "illegal_screen",
            "points":     0, "bucket": "none", "shot_type": "none",
            "shooter":    screener, "defender": None, "rebounder": None,
            "steal_by":   None, "block_by": None, "assist_by": None,
            "is_three":   False, "ft_made": 0, "ft_att": 0,
            "tech_type":  None, "flagrant_type": None,
            "ejected_player": None, "ejected_coach": None,
        }

    # --- FLAGRANT FOUL CHECK (defense commits) ---
    # Flagrant 1: excessive contact. Higher probability in blowouts,
    # late game desperation, high foul_tendency defenders.
    flag1_prob = FLAGRANT_1_BASE
    flag1_prob += max(0, abs(score_diff) - 10) * 0.0001   # blowouts
    flag1_prob += progress * 0.001                          # late game
    # High-foul-tendency defenders
    if defense_lineup:
        max_foul_tend = max(p.get("foul_tendency", 400) for p in defense_lineup)
        flag1_prob += (max_foul_tend - 400) / 1000.0 * 0.002

    if random.random() < flag1_prob and defense_lineup:
        fouler_scores = [max(1, p.get("foul_tendency", 400)) for p in defense_lineup]
        fouler = random.choices(defense_lineup, weights=fouler_scores, k=1)[0]
        fouler["foul_count"] = fouler.get("foul_count", 0) + 1
        accumulate_game_stats(fouler, "fouls")

        # Check for Flagrant 2 upgrade (second flagrant or severe)
        flagrant_count = fouler.get("flagrant_count", 0) + 1
        fouler["flagrant_count"] = flagrant_count

        if flagrant_count >= 2 or random.random() < FLAGRANT_2_BASE * 3:
            # FLAGRANT 2 -- ejection
            flagrant_type = "flagrant_2"
            ejected = fouler
            _eject_player(fouler, defense_team, game_state, defense_label)
        else:
            flagrant_type = "flagrant_1"
            ejected = None

        # 2 FTs + offense retains possession
        if offense_lineup:
            usage_scores = [max(1, p.get("usage_tendency", 10)) for p in offense_lineup]
            ft_shooter = random.choices(offense_lineup, weights=usage_scores, k=1)[0]
            ft_made, ft_att = _resolve_free_throws(ft_shooter, 2, burst_modifier)
            accumulate_game_stats(ft_shooter, "points",  ft_made)
            accumulate_game_stats(ft_shooter, "ft_made", ft_made)
            accumulate_game_stats(ft_shooter, "ft_att",  ft_att)
        else:
            ft_made = ft_att = 0
            ft_shooter = None

        return {
            "outcome":       "flagrant",
            "points":        ft_made, "bucket": "none", "shot_type": "none",
            "shooter":       ft_shooter, "defender": fouler,
            "rebounder":     None, "steal_by": None, "block_by": None,
            "assist_by":     None, "is_three": False,
            "ft_made":       ft_made, "ft_att": ft_att,
            "tech_type":     None,
            "flagrant_type": flagrant_type,
            "ejected_player": ejected, "ejected_coach": None,
            "retain_possession": True,   # offense keeps ball
        }

    # --- CLASS B TECHNICAL (administrative, non-personal) ---
    # Delay of game, hanging on rim after dunk, uniform violation, etc.
    # Either team can get a Class B -- slightly higher on offense (they touch the rim)
    classb_prob = TECH_CLASS_B_BASE
    # After a big dunk/finish, slight uptick in rim-hang chance
    if burst_modifier > 0.2:
        classb_prob += 0.001

    if random.random() < classb_prob:
        # Who gets it -- could be either team
        # Model: 55% offense (rim hang after dunk), 45% defense (delay, bench violation)
        if random.random() < 0.55 and offense_lineup:
            tech_team_lineup = offense_lineup
            tech_team        = offense_team
            tech_label       = offense_label
            opponent_lineup  = defense_lineup
        else:
            tech_team_lineup = defense_lineup
            tech_team        = defense_team
            tech_label       = defense_label
            opponent_lineup  = offense_lineup

        # Pick player -- low composure more likely
        comp_scores = [max(1, 20 - p.get("composure", 10)) for p in tech_team_lineup]
        tech_player = random.choices(tech_team_lineup, weights=comp_scores, k=1)[0]

        # Track technicals -- 2 of any class = ejection
        tech_player["tech_count"] = tech_player.get("tech_count", 0) + 1
        ejected_player = None
        if tech_player["tech_count"] >= 2:
            ejected_player = tech_player
            _eject_player(tech_player, tech_team, game_state, tech_label)

        # 1 FT + opponent retains/gains possession
        if opponent_lineup:
            usage_scores = [max(1, p.get("usage_tendency", 10)) for p in opponent_lineup]
            ft_shooter = random.choices(opponent_lineup, weights=usage_scores, k=1)[0]
            ft_made, ft_att = _resolve_free_throws(ft_shooter, 1, burst_modifier)
            accumulate_game_stats(ft_shooter, "points",  ft_made)
            accumulate_game_stats(ft_shooter, "ft_made", ft_made)
            accumulate_game_stats(ft_shooter, "ft_att",  ft_att)
        else:
            ft_made = ft_att = 0
            ft_shooter = None

        return {
            "outcome":       "technical_b",
            "points":        ft_made, "bucket": "none", "shot_type": "none",
            "shooter":       ft_shooter, "defender": tech_player,
            "rebounder":     None, "steal_by": None, "block_by": None,
            "assist_by":     None, "is_three": False,
            "ft_made":       ft_made, "ft_att": ft_att,
            "tech_type":     "class_b",
            "flagrant_type": None,
            "ejected_player": ejected_player, "ejected_coach": None,
            "retain_possession": False,  # opponent gets possession after
        }

    # --- CLASS A TECHNICAL (personal conduct) ---
    # Arguing with refs, taunting, obscenities -- player OR coach
    classa_prob = TECH_CLASS_A_BASE
    # Late game, close game or collapse: frustration spikes
    classa_prob += progress * 0.002
    if abs(score_diff) <= 5:
        classa_prob += 0.001   # tight game tension
    if burst_modifier < -0.2:
        classa_prob += 0.002   # collapse state -- players losing composure
    if abs(score_diff) >= 20:
        classa_prob += 0.001   # blowout frustration

    if random.random() < classa_prob:
        # 70% player technical, 30% coach technical
        is_coach_tech = (random.random() < 0.30)

        ejected_player = None
        ejected_coach  = None

        if is_coach_tech:
            # Coach technical
            # Low game_management coaches argue more
            for team_label, team, opponent_lineup_ref in [
                (offense_label, offense_team, defense_lineup),
                (defense_label, defense_team, offense_lineup),
            ]:
                coach = team.get("coach", {}) if team else {}
                gm    = coach.get("game_management", 10)
                # Low GM coach is more likely to get a tech
                coach_tech_prob = max(0, (12 - gm) / 12.0 * 0.6)
                if random.random() < coach_tech_prob:
                    coach["tech_count"] = coach.get("tech_count", 0) + 1
                    if coach["tech_count"] >= 2:
                        ejected_coach = coach
                        _eject_coach(coach, team, game_state, team_label)
                    ft_team_lineup = opponent_lineup_ref
                    tech_source    = "coach"
                    break
            else:
                # No coach got it -- fall through to player tech
                is_coach_tech = False

        if not is_coach_tech:
            # Player technical -- low composure + low basketball_iq
            # Either team's player can get it
            all_players = []
            for p in offense_lineup:
                all_players.append((p, offense_team, offense_label, defense_lineup))
            for p in defense_lineup:
                all_players.append((p, defense_team, defense_label, offense_lineup))

            tech_scores = []
            for p, _, _, _ in all_players:
                comp = p.get("composure",     10)
                iq   = p.get("basketball_iq", 10)
                # Low composure + low IQ = more likely to mouth off
                score = max(1, (20 - comp) * 1.5 + (14 - iq) * 0.5)
                tech_scores.append(score)

            idx = random.choices(range(len(all_players)), weights=tech_scores, k=1)[0]
            tech_player, tech_team_ref, tech_label, opponent_lineup_ref = all_players[idx]

            tech_player["tech_count"] = tech_player.get("tech_count", 0) + 1
            # Class A counts as personal foul
            tech_player["foul_count"] = tech_player.get("foul_count", 0) + 1
            accumulate_game_stats(tech_player, "fouls")

            if tech_player["tech_count"] >= 2:
                ejected_player = tech_player
                _eject_player(tech_player, tech_team_ref, game_state, tech_label)

            ft_team_lineup = opponent_lineup_ref
            tech_source    = "player"

        # 2 FTs + opponent retains/gains possession
        if ft_team_lineup:
            usage_scores = [max(1, p.get("usage_tendency", 10)) for p in ft_team_lineup]
            ft_shooter = random.choices(ft_team_lineup, weights=usage_scores, k=1)[0]
            ft_made, ft_att = _resolve_free_throws(ft_shooter, 2, burst_modifier)
            accumulate_game_stats(ft_shooter, "points",  ft_made)
            accumulate_game_stats(ft_shooter, "ft_made", ft_made)
            accumulate_game_stats(ft_shooter, "ft_att",  ft_att)
        else:
            ft_made = ft_att = 0
            ft_shooter = None

        return {
            "outcome":       "technical_a",
            "points":        ft_made, "bucket": "none", "shot_type": "none",
            "shooter":       ft_shooter, "defender": None,
            "rebounder":     None, "steal_by": None, "block_by": None,
            "assist_by":     None, "is_three": False,
            "ft_made":       ft_made, "ft_att": ft_att,
            "tech_type":     "class_a",
            "flagrant_type": None,
            "ejected_player": ejected_player, "ejected_coach": ejected_coach,
            "retain_possession": False,  # opponent gets/keeps possession
        }

    return None  # no technical/flagrant/illegal screen this possession


def _eject_player(player, team, game_state, team_label):
    """
    Ejects a player. Marks them ejected -- they are removed from
    lineup by _remove_fouled_out (which checks ejected flag).
    Sets foul_count to 99 so they are always removed.
    """
    player["ejected"]   = True
    player["foul_count"] = 99   # forces removal from all lineups
    if game_state is not None:
        key = team_label + "_ejected_player"
        if key not in game_state:
            game_state[key] = []
        game_state[key].append(player.get("name", "Unknown"))


def _eject_coach(coach, team, game_state, team_label):
    """
    Ejects a coach. Assistant takes over with reduced game_management.
    Marks ejected on coach dict. Burst system checks this.
    """
    coach["ejected"] = True
    # Assistant coach game_management: base 8, slightly modified by staff_development
    staff_dev = team.get("coach", {}).get("staff_development", 10) if team else 10
    asst_gm   = min(12, 6 + int(staff_dev / 3))
    coach["effective_game_management"] = asst_gm
    if game_state is not None:
        game_state[team_label + "_coach_ejected"] = True


# -----------------------------------------
# POSSESSION RESOLUTION (Phase B core)
# -----------------------------------------

def simulate_possession(offense_lineup, defense_lineup,
                        offense_profile, defense_profile,
                        momentum=0, fatigue=0,
                        possession_num=0, total_possessions=68,
                        burst_modifier=0.0,
                        game_state=None, offense_label="home"):
    """
    Simulates one offensive possession.
    Phase B: possession state -> drive check -> bucket selection
             -> shot resolution -> foul/free throw -> rebound -> assist

    game_state: used for team foul tracking and bonus enforcement.
    offense_label: "home" or "away" -- identifies which team is on offense.
                   The DEFENSE is the other team, so defense fouls accumulate
                   on the defending team's counter.
    """
    # Defense label for team foul tracking
    defense_label = "away" if offense_label == "home" else "home"
    empty = {"outcome": "turnover", "points": 0, "shot_type": "none",
             "bucket": "none", "shooter": None, "defender": None,
             "rebounder": None, "steal_by": None, "block_by": None,
             "assist_by": None, "is_three": False,
             "ft_made": 0, "ft_att": 0}

    if not offense_lineup or not defense_lineup:
        return empty

    # --- BONUS FOUL CHECK (non-shooting contact) ---
    # Before any possession action, check if defense commits a non-shooting foul.
    # This only triggers FTs if the defending team is in the bonus.
    # Frequency: ~4% per possession (roughly 3-5 bonus fouls/team/game).
    # These are hand-checks, holds, bumps away from ball -- not shot fouls.
    if game_state is not None:
        def_team_foul_key = defense_label + "_team_fouls"
        team_fouls_now = game_state.get(def_team_foul_key, 0)

        # Non-shooting foul probability -- slightly higher late in half (fatigue/desperation)
        nonshooting_foul_prob = 0.046 + fatigue * 0.010
        # Defense pressure increases non-shooting foul chance
        def_pressure = defense_profile.get("pressure", 50)
        nonshooting_foul_prob += (def_pressure - 50) / 100.0 * 0.01

        if random.random() < nonshooting_foul_prob:
            # Find who commits the foul (weighted by foul_tendency)
            if defense_lineup:
                foul_scores = [max(1, p.get("foul_tendency", 400)) for p in defense_lineup]
                fouler = random.choices(defense_lineup, weights=foul_scores, k=1)[0]
                fouler["foul_count"] = fouler.get("foul_count", 0) + 1
                accumulate_game_stats(fouler, "fouls")

            # Increment team foul counter
            game_state[def_team_foul_key] = team_fouls_now + 1
            team_fouls_after = team_fouls_now + 1

            # Check bonus status
            if team_fouls_after >= 10:
                # DOUBLE BONUS: automatic 2 FTs
                # Foul shooter is the player with ball (pick from offense weighted by usage)
                usage_scores = [max(1, p.get("usage_tendency", 10)) for p in offense_lineup]
                ft_shooter = random.choices(offense_lineup, weights=usage_scores, k=1)[0]
                ft_made, ft_att = _resolve_free_throws(ft_shooter, 2, burst_modifier)
                accumulate_game_stats(ft_shooter, "points",  ft_made)
                accumulate_game_stats(ft_shooter, "ft_made", ft_made)
                accumulate_game_stats(ft_shooter, "ft_att",  ft_att)
                return {**empty, "outcome": "foul", "points": ft_made,
                        "ft_made": ft_made, "ft_att": ft_att,
                        "shooter": ft_shooter}

            elif team_fouls_after >= 7:
                # BONUS (1-and-1): make first to get second
                usage_scores = [max(1, p.get("usage_tendency", 10)) for p in offense_lineup]
                ft_shooter = random.choices(offense_lineup, weights=usage_scores, k=1)[0]
                ft_made, ft_att = _resolve_free_throws_one_and_one(ft_shooter, burst_modifier)
                accumulate_game_stats(ft_shooter, "points",  ft_made)
                accumulate_game_stats(ft_shooter, "ft_made", ft_made)
                accumulate_game_stats(ft_shooter, "ft_att",  ft_att)
                return {**empty, "outcome": "foul", "points": ft_made,
                        "ft_made": ft_made, "ft_att": ft_att,
                        "shooter": ft_shooter}
            # else: before bonus -- foul with no FTs, just stops possession
            # (possession resets, no points -- return turnover-like outcome)
            return {**empty, "outcome": "foul_no_bonus", "points": 0}

    # --- TECHNICAL / FLAGRANT / ILLEGAL SCREEN CHECK ---
    # Rare events that can fire on any possession before normal resolution.
    score_diff = game_state.get("score_diff", 0) if game_state else 0
    tech_result = _check_technical_fouls(
        offense_lineup, defense_lineup,
        offense_team=None,
        defense_team=None,
        game_state=game_state,
        offense_label=offense_label,
        burst_modifier=burst_modifier,
        score_diff=score_diff,
        possession_num=possession_num,
        total_possessions=total_possessions,
    )
    if tech_result is not None:
        return tech_result

    # --- TURNOVER CHECK ---
    # Offensive TO rate + defensive pressure modifier
    off_to_base = offense_profile.get("base_to_rate", 0.14)
    def_pressure = defense_profile.get("pressure", 50)
    pressure_mod = (def_pressure - 50) / 100.0 * 0.06
    to_rate = off_to_base + pressure_mod

    # Burst affects turnover rate
    if burst_modifier < 0:
        to_rate += abs(burst_modifier) * 0.06  # collapse/complacency
    elif burst_modifier > 0:
        to_rate -= burst_modifier * 0.04        # composure run

    to_rate = max(0.08, min(0.32, to_rate))

    if random.random() < to_rate:
        to_result = _resolve_turnover(
            offense_lineup, defense_lineup,
            pressure_mod=pressure_mod,
            burst_modifier=burst_modifier
        )
        return {**empty,
                "shooter":  to_result["ball_handler"],
                "steal_by": to_result["steal_by"],
                "to_type":  to_result["to_type"]}

    # --- POSSESSION STATE ---
    poss_state = _determine_possession_state(
        offense_lineup, defense_lineup, offense_profile
    )

    # --- DRIVE MODEL ---
    is_catch_situation = False
    preferred_bucket   = None

    if poss_state == "drive":
        # Select ball handler and their defender
        pg_or_sg = [p for p in offense_lineup
                    if p.get("position") in ("PG", "SG", "SF")]
        ball_handler  = (pg_or_sg[0] if pg_or_sg
                         else random.choice(offense_lineup))
        primary_def   = _select_defender(ball_handler, defense_lineup)
        rim_protector = _find_rim_protector(defense_lineup)

        drive_result  = _resolve_drive(
            ball_handler, primary_def, offense_lineup,
            defense_lineup, rim_protector
        )

        if drive_result["kick_out"]:
            # Drive kick-out: open perimeter shot
            poss_state         = "ball_movement"
            is_catch_situation = True
            preferred_bucket   = "corner_three" if random.random() < 0.55 else "above_break_three"
        elif drive_result["success"]:
            preferred_bucket = drive_result["preferred_bucket"]
        # If drive failed, fall through to normal half court

    elif poss_state == "ball_movement":
        is_catch_situation = True

    elif poss_state == "post_up":
        is_catch_situation = True

    # --- BUCKET SELECTION ---
    bucket = _select_shot_bucket(
        poss_state, offense_lineup[0],  # temporary, overridden by shooter selection
        offense_lineup, defense_lineup,
        offense_profile, preferred_bucket
    )

    # --- SHOOTER SELECTION ---
    shooter  = _select_shooter_for_bucket(offense_lineup, bucket, poss_state)
    defender = _select_defender(shooter, defense_lineup)

    # --- SHOT RESOLUTION ---
    # Position established: defender is late, offense has body position
    # Fires on: clean post catches, cutters/roll men at rim off ball movement,
    # drive kick-outs that lead to at_rim dump-offs
    pos_established = False
    if bucket == "post" and poss_state == "post_up" and is_catch_situation:
        # Big caught it clean at the block with position
        pos_established = True
    elif bucket == "at_rim" and poss_state == "ball_movement" and is_catch_situation:
        # Cutter or roll man receiving pass at the rim
        pos_established = True
    elif bucket == "at_rim" and poss_state == "drive" and is_catch_situation:
        # Drive kick-out to a cutter crashing
        pos_established = True
    elif bucket == "at_rim" and poss_state == "transition":
        # Transition layup -- defense scrambling back
        pos_established = random.random() < 0.65

    result, points, is_three, ft_made, ft_att, block_by, block_outcome = resolve_shot_bucket(
        shooter, defender, bucket,
        possession_state=poss_state,
        is_catch_situation=is_catch_situation,
        fatigue=fatigue,
        momentum=momentum,
        burst_modifier=burst_modifier,
        defense_lineup=defense_lineup,
        position_established=pos_established,
    )

    # --- PROCESS RESULT ---

    if result in ("make", "make_and1"):
        accumulate_game_stats(shooter, "points", points)
        accumulate_game_stats(shooter, "fg_made")
        accumulate_game_stats(shooter, "fg_att")
        if is_three:
            accumulate_game_stats(shooter, "three_made")
            accumulate_game_stats(shooter, "three_att")

        # Assist check
        assist_player, has_assist = _check_assist(
            poss_state, shooter, offense_lineup, is_catch_situation
        )
        if has_assist and assist_player:
            accumulate_game_stats(assist_player, "assists")

        # AND-1: foul on the make -- 1 free throw
        and1_ft_made = 0
        and1_ft_att  = 0
        if result == "make_and1" and defender:
            defender["foul_count"] = defender.get("foul_count", 0) + 1
            accumulate_game_stats(defender, "fouls")
            and1_ft_made, and1_ft_att = _resolve_free_throws(shooter, 1, burst_modifier)
            accumulate_game_stats(shooter, "points",  and1_ft_made)
            accumulate_game_stats(shooter, "ft_made", and1_ft_made)
            accumulate_game_stats(shooter, "ft_att",  and1_ft_att)

        total_pts = points + and1_ft_made
        return {"outcome": "score", "points": total_pts, "bucket": bucket,
                "shot_type": bucket, "shooter": shooter,
                "defender": defender, "rebounder": None,
                "steal_by": None, "block_by": None,
                "assist_by": assist_player, "is_three": is_three,
                "ft_made": and1_ft_made, "ft_att": and1_ft_att}

    elif result == "foul":
        # --- TEAM FOUL TRACKING ---
        # Defense commits the foul. Increment defending team's half foul count.
        def_team_foul_key = defense_label + "_team_fouls"
        if game_state is not None:
            game_state[def_team_foul_key] = game_state.get(def_team_foul_key, 0) + 1
        team_fouls = game_state.get(def_team_foul_key, 0) if game_state else 0

        if defender:
            defender["foul_count"] = defender.get("foul_count", 0) + 1
            accumulate_game_stats(defender, "fouls")

        # --- DETERMINE FREE THROW COUNT ---
        # Shooting fouls: always get FTs based on shot type
        #   - Three-point shooting foul: 3 FTs
        #   - All other shooting fouls: 2 FTs
        #   - AND-1 situation: handled separately in make path
        # Non-shooting fouls in bonus:
        #   - 7-9 team fouls: 1-and-1 (make first = attempt second)
        #   - 10+ team fouls: automatic 2 FTs (double bonus)
        # Non-shooting fouls before bonus: no FTs, just possession reset

        # This is a shooting foul (foul during shot attempt)
        # All fouls from resolve_shot_bucket are shooting fouls --
        # they fire during shot resolution. Non-shooting fouls
        # are bonus fouls triggered separately below.
        is_shooting_foul = True  # all fouls from shot resolution are shooting fouls

        accumulate_game_stats(shooter, "fg_att")
        if is_three:
            accumulate_game_stats(shooter, "three_att")

        if is_three:
            n_ft = 3   # three-point shooting foul: always 3 FTs
        else:
            n_ft = 2   # standard shooting foul: 2 FTs

        ft_made, ft_att = _resolve_free_throws(shooter, n_ft, burst_modifier)

        accumulate_game_stats(shooter, "points",  ft_made)
        accumulate_game_stats(shooter, "ft_made", ft_made)
        accumulate_game_stats(shooter, "ft_att",  ft_att)

        return {"outcome": "foul", "points": ft_made, "bucket": bucket,
                "shot_type": bucket, "shooter": shooter,
                "defender": defender, "rebounder": None,
                "steal_by": None, "block_by": None,
                "assist_by": None, "is_three": is_three,
                "ft_made": ft_made, "ft_att": ft_att}

    else:  # miss
        accumulate_game_stats(shooter, "fg_att")
        if is_three:
            accumulate_game_stats(shooter, "three_att")

        if block_by:
            accumulate_game_stats(block_by, "blocks")

        # Rebound
        reb_type, rebounder = resolve_rebound(
            offense_lineup, defense_lineup,
            off_rebound_bonus=offense_profile["off_rebound_bonus"],
            rebounding_mod=offense_profile.get("rebounding_mod", 0.0)
        )
        if rebounder:
            accumulate_game_stats(rebounder, "rebounds")

        # Putback opportunity
        if reb_type == "offensive" and rebounder:
            pb_defender     = _select_defender(rebounder, defense_lineup)
            pb_result, pb_pts, _, _, _, pb_blk, _pb_bo = resolve_shot_bucket(
                rebounder, pb_defender, "putback",
                fatigue=fatigue + 0.15, burst_modifier=burst_modifier,
                defense_lineup=defense_lineup,
                position_established=True,  # already at the rim
            )
            if pb_result == "make":
                accumulate_game_stats(rebounder, "points",  pb_pts)
                accumulate_game_stats(rebounder, "fg_made")
                accumulate_game_stats(rebounder, "fg_att")
                return {"outcome": "score", "points": pb_pts, "bucket": "putback",
                        "shot_type": "putback", "shooter": rebounder,
                        "defender": pb_defender, "rebounder": rebounder,
                        "steal_by": None, "block_by": block_by,
                        "assist_by": None, "is_three": False,
                        "ft_made": 0, "ft_att": 0}
            elif pb_result == "foul":
                ft_made, ft_att = _resolve_free_throws(rebounder, 2, burst_modifier)
                if pb_defender:
                    pb_defender["foul_count"] = pb_defender.get("foul_count", 0) + 1
                    accumulate_game_stats(pb_defender, "fouls")
                accumulate_game_stats(rebounder, "points",  ft_made)
                accumulate_game_stats(rebounder, "ft_made", ft_made)
                accumulate_game_stats(rebounder, "ft_att",  ft_att)
                accumulate_game_stats(rebounder, "fg_att")
                return {"outcome": "foul", "points": ft_made, "bucket": "putback",
                        "shot_type": "putback", "shooter": rebounder,
                        "defender": pb_defender, "rebounder": rebounder,
                        "steal_by": None, "block_by": block_by,
                        "assist_by": None, "is_three": False,
                        "ft_made": ft_made, "ft_att": ft_att}
            else:
                accumulate_game_stats(rebounder, "fg_att")

        return {"outcome": "miss", "points": 0, "bucket": bucket,
                "shot_type": bucket, "shooter": shooter,
                "defender": defender, "rebounder": rebounder,
                "steal_by": None, "block_by": block_by,
                "assist_by": None, "is_three": is_three,
                "ft_made": 0, "ft_att": 0}



