def _resolve_turnover(offense_lineup, defense_lineup,
                       pressure_mod=0.0, burst_modifier=0.0):
    """
    Generates a turnover and attributes it.
    Returns dict with turnover_type, ball_handler, steal_by.

    Turnover types: bad_pass, ball_handling, charge, shot_clock, intercepted
    """
    # Turnover type distribution
    to_type_weights = {
        "bad_pass":      28,   # live ball -- sometimes stolen
        "ball_handling": 18,   # live ball -- sometimes stripped
        "travel":        10,   # dead ball -- never stolen
        "charge":         8,   # dead ball -- never stolen
        "shot_clock":    16,   # dead ball -- never stolen
        "intercepted":   20,   # live ball -- always stolen
    }

    # Burst modifier shifts toward bad_pass / shot_clock
    if burst_modifier < -0.2:  # collapse
        to_type_weights["bad_pass"]   += 8
        to_type_weights["shot_clock"] += 5

    to_types = list(to_type_weights.keys())
    to_wts   = list(to_type_weights.values())
    to_type  = random.choices(to_types, weights=to_wts, k=1)[0]

    # Who turns it over (weighted by inverse ball handling)
    to_scores = []
    for player in offense_lineup:
        bh = player.get("ball_handling", 400)
        dm = player.get("decision_making", 400)
        # Bad handlers and poor decision makers turn it over more
        to_scores.append(max(50, (1000 - bh) * 0.6 + (1000 - dm) * 0.4))

    ball_handler = random.choices(offense_lineup, weights=to_scores, k=1)[0]

    # Steal attribution
    steal_by = None
    # Dead-ball turnovers (travel, charge, shot_clock) never produce steals
    steal_types_with_steals = {
        "bad_pass":      0.50,
        "intercepted":   0.90,
        "ball_handling": 0.35,
        # travel, charge, shot_clock intentionally absent -- dead ball
    }

    steal_chance = steal_types_with_steals.get(to_type, 0.0)
    if steal_chance > 0 and random.random() < steal_chance:
        steal_by = _select_steal_player(defense_lineup, steal_type=to_type)
        if steal_by:
            accumulate_game_stats(steal_by, "steals")

    accumulate_game_stats(ball_handler, "turnovers")

    return {
        "to_type":     to_type,
        "ball_handler": ball_handler,
        "steal_by":    steal_by,
    }


