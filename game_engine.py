# BURST SYSTEM (C9)
# -----------------------------------------

def _check_burst(home_score, away_score, possession_num, total_possessions,
                 home_team, away_team, game_state):
    """
    Checks whether a burst event fires this possession.
    Returns updated game_state dict.

    game_state tracks:
      home_burst_active, away_burst_active
      home_burst_type, away_burst_type
      home_burst_remaining, away_burst_remaining
      burst_cooldown_home, burst_cooldown_away
      home_timeouts, away_timeouts
    """
    if "home_timeouts" not in game_state:
        game_state["home_timeouts"] = 4
        game_state["away_timeouts"] = 4
        game_state["burst_cooldown_home"] = 0
        game_state["burst_cooldown_away"] = 0

    # Decrement burst counters
    if game_state.get("home_burst_active"):
        game_state["home_burst_remaining"] -= 1
        if game_state["home_burst_remaining"] <= 0:
            game_state["home_burst_active"] = False
            game_state["burst_cooldown_home"] = 15

    if game_state.get("away_burst_active"):
        game_state["away_burst_remaining"] -= 1
        if game_state["away_burst_remaining"] <= 0:
            game_state["away_burst_active"] = False
            game_state["burst_cooldown_away"] = 15

    # Decrement cooldowns
    if game_state["burst_cooldown_home"] > 0:
        game_state["burst_cooldown_home"] -= 1
    if game_state["burst_cooldown_away"] > 0:
        game_state["burst_cooldown_away"] -= 1

    gap = home_score - away_score

    for team_label, score_diff, team in [
        ("home", gap,  home_team),
        ("away", -gap, away_team),
    ]:
        burst_key     = team_label + "_burst_active"
        cooldown_key  = "burst_cooldown_" + team_label
        type_key      = team_label + "_burst_type"
        remain_key    = team_label + "_burst_remaining"

        if game_state.get(burst_key):
            continue  # Already in burst
        if game_state[cooldown_key] > 0:
            continue  # In cooldown

        if abs(score_diff) < 10:
            continue  # Gap not large enough

        # Roster composition modifiers
        roster = team.get("roster", [])
        senior_pct = (sum(1 for p in roster if p.get("year") == "Senior")
                      / max(1, len(roster)))
        fresh_pct  = (sum(1 for p in roster if p.get("year") == "Freshman")
                      / max(1, len(roster)))
        coach      = team.get("coach", {})
        gm         = coach.get("game_management", 10) / 20.0
        is_home    = team_label == "home"
        progress   = possession_num / max(1, total_possessions)
        is_tourney  = game_state.get("is_tournament", False)

        if score_diff < 0:
            # Team is LOSING -- composure run vs collapse check
            composure_prob = 0.17
            composure_prob += senior_pct * 0.08
            composure_prob -= fresh_pct  * 0.06
            composure_prob += gm         * 0.06
            composure_prob += 0.03 if is_home else -0.03
            composure_prob += 0.02 if is_tourney else 0
            composure_prob -= max(0, (-score_diff - 15)) * 0.008  # big deficits harder

            collapse_prob  = 0.17
            collapse_prob -= senior_pct * 0.06
            collapse_prob += fresh_pct  * 0.06
            collapse_prob -= gm         * 0.04

            r = random.random()
            if r < composure_prob:
                game_state[burst_key]   = True
                game_state[type_key]    = "composure_run"
                game_state[remain_key]  = random.randint(10, 13)
            elif r < composure_prob + collapse_prob:
                game_state[burst_key]   = True
                game_state[type_key]    = "collapse"
                game_state[remain_key]  = random.randint(8, 12)

        else:
            # Team is WINNING -- lock in vs complacency
            lock_in_prob   = 0.12
            lock_in_prob  += senior_pct * 0.05
            lock_in_prob  += gm         * 0.04

            complacency_prob = 0.12
            complacency_prob += fresh_pct * 0.05
            complacency_prob -= gm        * 0.03

            r = random.random()
            if r < lock_in_prob:
                game_state[burst_key]   = True
                game_state[type_key]    = "lock_in"
                game_state[remain_key]  = random.randint(8, 12)
            elif r < lock_in_prob + complacency_prob:
                game_state[burst_key]   = True
                game_state[type_key]    = "complacency"
                game_state[remain_key]  = random.randint(8, 12)

    return game_state


def _get_burst_modifier(game_state, team_label):
    """
    Returns a float modifier based on active burst state.
    Positive = beneficial (composure run, lock in)
    Negative = detrimental (collapse, complacency)
    Applied to turnover rate and shot quality.
    """
    burst_key = team_label + "_burst_active"
    type_key  = team_label + "_burst_type"

    if not game_state.get(burst_key):
        return 0.0

    burst_type = game_state.get(type_key, "")
    return {
        "composure_run": +0.4,
        "lock_in":       +0.3,
        "collapse":      -0.4,
        "complacency":   -0.25,
    }.get(burst_type, 0.0)


