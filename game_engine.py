# HALFTIME RECOVERY
# -----------------------------------------

def _apply_halftime_recovery(team):
    """
    Partial stamina recovery at halftime.
    Players recover ~60% of accumulated fatigue.
    Starters recover less than bench (they played more minutes).
    This affects fatigue_base in the second half.

    In practice: we store accumulated_fatigue on each player
    this game, and halftime reduces it. Since fatigue is currently
    calculated from possession number, we model recovery by
    returning a fatigue_offset that second half simulation applies.

    Recovery model:
      - All players: reset first-half fatigue accumulation by 60%
      - High endurance players recover more (up to 70%)
      - Low endurance players recover less (down to 45%)
      - Starters who played 20+ first-half minutes recover slightly less
    """
    allocation = team.get("minutes_allocation", {})
    roster     = team.get("roster", [])

    for player in roster:
        endurance = player.get("endurance", 500)
        # Base recovery: 60%
        recovery_pct = 0.60 + (endurance - 500) / 500.0 * 0.10
        recovery_pct = max(0.45, min(0.72, recovery_pct))
        # Store on player for second half fatigue calculation
        player["halftime_recovery"] = recovery_pct

    return team


def _get_halftime_fatigue_base(player):
    """
    Returns adjusted fatigue base for second half.
    Players who didn't recover fully start second half more tired.
    Typical range: 0.08 - 0.20 (vs 0.0 at game start).
    """
    recovery = player.get("halftime_recovery", 0.60)
    # First half fatigue peak is ~0.5 at end of half
    # Recovery reduces that: remaining fatigue = 0.5 * (1 - recovery)
    residual = 0.5 * (1.0 - recovery)
    return max(0.0, residual)

# -----------------------------------------
# PERIOD SIMULATION
# -----------------------------------------

def simulate_period(home_lineup, away_lineup, home_bench, away_bench,
                    possessions, home_score, away_score,
                    home_momentum, away_momentum,
                    home_profile, away_profile,
                    home_coach, away_coach,
                    fatigue_base=0, game_state=None,
                    home_team=None, away_team=None,
                    half_number=1):
    """
    Simulates one half (20 minutes) with individual players, rotation,
    team foul tracking, bonus/double-bonus enforcement, and burst system.

    half_number: 1 or 2. Second half uses halftime_recovery for fatigue base.
    Team fouls tracked in game_state["home_team_fouls"] / ["away_team_fouls"].
    Reset to 0 at start of each half by simulate_game.
    """
    if game_state is None:
        game_state = {
            "home_burst_active": False, "away_burst_active": False,
            "home_burst_type": None,    "away_burst_type": None,
            "home_burst_remaining": 0,  "away_burst_remaining": 0,
            "burst_cooldown_home": 0,   "burst_cooldown_away": 0,
            "home_timeouts": 4,         "away_timeouts": 4,
            "is_tournament": False,
            "home_team_fouls": 0,
            "away_team_fouls": 0,
        }

    home_rot_size = home_coach.get("rotation_size", 8) if home_coach else 8
    away_rot_size = away_coach.get("rotation_size", 8) if away_coach else 8
    home_rot_flex = home_coach.get("rotation_flexibility", 5) if home_coach else 5
    away_rot_flex = away_coach.get("rotation_flexibility", 5) if away_coach else 5

    mins_per_possession = 20.0 / max(1, possessions)  # each half is 20 minutes

    # Initialize team game stat trackers
    if home_team and "team_game_stats" not in home_team:
        home_team["team_game_stats"] = _empty_team_game_stats()
    if away_team and "team_game_stats" not in away_team:
        away_team["team_game_stats"] = _empty_team_game_stats()

    # Second half: use per-player halftime recovery for fatigue base
    # Average recovery across active players used as period fatigue floor
    def _period_fatigue_base(lineup, half_number, fatigue_base):
        if half_number == 1:
            return fatigue_base
        recoveries = [p.get("halftime_recovery", 0.60) for p in lineup]
        avg_recovery = sum(recoveries) / max(1, len(recoveries))
        residual = 0.5 * (1.0 - avg_recovery)
        return max(fatigue_base, residual)

    for i in range(possessions):
        home_fatigue_floor = _period_fatigue_base(home_lineup, half_number, fatigue_base)
        away_fatigue_floor = _period_fatigue_base(away_lineup, half_number, fatigue_base)
        fatigue = fatigue_base + (i / (possessions * 2))
        home_fatigue = max(home_fatigue_floor, fatigue)
        away_fatigue = max(away_fatigue_floor, fatigue)

        home_active = _get_rotation_segment(
            i, possessions, home_lineup, home_bench,
            home_rot_size, home_rot_flex)
        away_active = _get_rotation_segment(
            i, possessions, away_lineup, away_bench,
            away_rot_size, away_rot_flex)

        # Foul-out enforcement
        home_active = _remove_fouled_out(home_active, home_bench)
        away_active = _remove_fouled_out(away_active, away_bench)

        if not home_active:
            home_active = list(home_lineup)
        if not away_active:
            away_active = list(away_lineup)

        # Store score diff in game_state for tech/flagrant probability
        if game_state is not None:
            game_state["score_diff"] = home_score - away_score

        # Burst system
        if home_team and away_team:
            game_state = _check_burst(
                home_score, away_score, i, possessions,
                home_team, away_team, game_state
            )

        home_burst = _get_burst_modifier(game_state, "home")
        away_burst = _get_burst_modifier(game_state, "away")

        home_mom_mod = min(0.3, max(-0.3, home_momentum * 0.3))
        away_mom_mod = min(0.3, max(-0.3, away_momentum * 0.3))

        for p in home_active:
            accumulate_game_stats(p, "minutes", mins_per_possession)
        for p in away_active:
            accumulate_game_stats(p, "minutes", mins_per_possession)

        # Jump ball check (~1.5% per possession -- held ball, scramble)
        # Coin flip outcome -- winner retains/gets possession
        # Modeled as: ~50% chance each team gets possession (skip one side's possession)
        if random.random() < 0.015:
            # Jump ball: randomly award this possession to either team
            # Winner keeps their offensive possession, loser skips
            if random.random() < 0.5:
                # Home wins jump -- home gets extra possession this cycle, skip away
                if home_team and "team_game_stats" in home_team:
                    home_team["team_game_stats"]["team_reb_offense"] += 1
                home_result = simulate_possession(
                    home_active, away_active,
                    home_profile, away_profile,
                    home_mom_mod, home_fatigue, i, possessions,
                    burst_modifier=home_burst,
                    game_state=game_state, offense_label="home")
                home_score += home_result["points"]
                if home_result["outcome"] == "score":
                    home_momentum = min(1.0, home_momentum + 0.2)
                else:
                    home_momentum = max(-1.0, home_momentum - 0.1)
                continue  # skip away possession this cycle
            else:
                # Away wins jump -- away gets extra, skip home
                if away_team and "team_game_stats" in away_team:
                    away_team["team_game_stats"]["team_reb_offense"] += 1
                away_result = simulate_possession(
                    away_active, home_active,
                    away_profile, home_profile,
                    away_mom_mod, away_fatigue, i, possessions,
                    burst_modifier=away_burst,
                    game_state=game_state, offense_label="away")
                away_score += away_result["points"]
                if away_result["outcome"] == "score":
                    away_momentum = min(1.0, away_momentum + 0.2)
                else:
                    away_momentum = max(-1.0, away_momentum - 0.1)
                continue  # skip home possession this cycle

        # Home offense
        home_result = simulate_possession(
            home_active, away_active,
            home_profile, away_profile,
            home_mom_mod, home_fatigue, i, possessions,
            burst_modifier=home_burst,
            game_state=game_state, offense_label="home")
        home_score += home_result["points"]

        # Team stat tracking for special possession outcomes
        if home_team and "team_game_stats" in home_team:
            tgs = home_team["team_game_stats"]
            tr  = home_result.get("team_rebound")
            if tr == "offense":
                tgs["team_reb_offense"] += 1
            elif tr == "defense":
                tgs["team_reb_defense"] += 1
            to_type = home_result.get("to_type", "")
            if to_type in ("travel", "charge", "shot_clock"):
                tgs["dead_ball_tos"] += 1
            elif to_type in ("bad_pass", "ball_handling", "intercepted"):
                tgs["live_ball_tos"] += 1

        if home_result["outcome"] == "score":
            home_momentum = min(1.0, home_momentum + 0.2)
            away_momentum = max(-1.0, away_momentum - 0.1)
        else:
            home_momentum = max(-1.0, home_momentum - 0.1)

        # Away offense
        away_result = simulate_possession(
            away_active, home_active,
            away_profile, home_profile,
            away_mom_mod, away_fatigue, i, possessions,
            burst_modifier=away_burst,
            game_state=game_state, offense_label="away")
        away_score += away_result["points"]

        # Team stat tracking for away
        if away_team and "team_game_stats" in away_team:
            tgs = away_team["team_game_stats"]
            tr  = away_result.get("team_rebound")
            if tr == "offense":
                tgs["team_reb_offense"] += 1
            elif tr == "defense":
                tgs["team_reb_defense"] += 1
            to_type = away_result.get("to_type", "")
            if to_type in ("travel", "charge", "shot_clock"):
                tgs["dead_ball_tos"] += 1
            elif to_type in ("bad_pass", "ball_handling", "intercepted"):
                tgs["live_ball_tos"] += 1

        if away_result["outcome"] == "score":
            away_momentum = min(1.0, away_momentum + 0.2)
            home_momentum = max(-1.0, home_momentum - 0.1)
        else:
            away_momentum = max(-1.0, away_momentum - 0.1)

    return home_score, away_score, home_momentum, away_momentum, game_state


# -----------------------------------------
# MAIN GAME SIMULATOR
# -----------------------------------------

def simulate_game(home_team, away_team, possessions=None, verbose=True,
                  season_year=2024, is_tournament=False):
    """
    Simulates a full game. Phase B engine.
    Returns game result dict.
    """
    from cohesion import get_cohesion_modifiers

    # Legacy fallback
    if "roster" not in home_team or "roster" not in away_team:
        return _simulate_game_legacy(home_team, away_team, possessions, verbose)

    home_lineup, home_bench = build_lineup(home_team)
    away_lineup, away_bench = build_lineup(away_team)

    if not home_lineup or not away_lineup:
        return _simulate_game_legacy(home_team, away_team, possessions, verbose)

    # Reset foul counts, techs, ejections, flagrants for this game
    for p in home_team.get("roster", []):
        p["foul_count"]    = 0
        p["tech_count"]    = 0
        p["flagrant_count"] = 0
        p["ejected"]        = False
    for p in away_team.get("roster", []):
        p["foul_count"]    = 0
        p["tech_count"]    = 0
        p["flagrant_count"] = 0
        p["ejected"]        = False
    # Reset coach tech/ejection state
    for team in [home_team, away_team]:
        coach = team.get("coach", {})
        if coach:
            coach["tech_count"] = 0
            coach["ejected"]    = False
            coach.pop("effective_game_management", None)
    home_team["team_game_stats"] = _empty_team_game_stats()
    away_team["team_game_stats"] = _empty_team_game_stats()

    home_coach = home_team.get("coach", {})
    away_coach = away_team.get("coach", {})

    home_cohesion = get_cohesion_modifiers(home_team)
    away_cohesion = get_cohesion_modifiers(away_team)

    home_profile = build_game_profile(home_team)
    away_profile = build_game_profile(away_team)
    home_profile = _apply_cohesion_to_profile(home_profile, home_cohesion)
    away_profile = _apply_cohesion_to_profile(away_profile, away_cohesion)

    if possessions is None:
        base_possessions = 68
        pace_adj = (home_profile["possessions_modifier"] +
                    away_profile["possessions_modifier"]) // 2
        possessions = max(55, min(82, base_possessions + pace_adj))

    game_state = {
        "home_burst_active": False, "away_burst_active": False,
        "home_burst_type": None,    "away_burst_type": None,
        "home_burst_remaining": 0,  "away_burst_remaining": 0,
        "burst_cooldown_home": 0,   "burst_cooldown_away": 0,
        "home_timeouts": 4,         "away_timeouts": 4,
        "is_tournament": is_tournament,
        # Team foul tracking per half (resets at halftime)
        "home_team_fouls": 0,
        "away_team_fouls": 0,
        "score_diff": 0,
        "home_coach_ejected": False,
        "away_coach_ejected": False,
    }

    home_momentum = 0
    away_momentum = 0
    home_name     = home_team.get("name", "Home")
    away_name     = away_team.get("name", "Away")

    if verbose:
        print("")
        print(home_name + " vs " + away_name)
        home_coh = home_team.get("cohesion_tier", "?")
        away_coh = away_team.get("cohesion_tier", "?")
        print("  " + home_coach.get("archetype", "?").replace("_", " ") +
              " vs " + away_coach.get("archetype", "?").replace("_", " ") +
              "  |  " + str(possessions) + " poss" +
              "  |  cohesion: " + home_coh + " / " + away_coh)
        print("  Starters: " +
              ", ".join(p["name"].split()[0] + "(" + p["position"] + ")"
                        for p in home_lineup) + "  vs  " +
              ", ".join(p["name"].split()[0] + "(" + p["position"] + ")"
                        for p in away_lineup))
        print("----------------------------------------")

    half_possessions = possessions // 2
    remainder        = possessions - half_possessions * 2

    # --- FIRST HALF ---
    game_state["home_team_fouls"] = 0
    game_state["away_team_fouls"] = 0

    home_score, away_score, home_momentum, away_momentum, game_state = simulate_period(
        home_lineup, away_lineup, home_bench, away_bench,
        half_possessions, 0, 0, home_momentum, away_momentum,
        home_profile, away_profile, home_coach, away_coach,
        fatigue_base=0, game_state=game_state,
        home_team=home_team, away_team=away_team,
        half_number=1)

    if verbose:
        print("  Halftime: " + home_name + " " + str(home_score) +
              ", " + away_name + " " + str(away_score))

    # --- HALFTIME RECOVERY ---
    _apply_halftime_recovery(home_team)
    _apply_halftime_recovery(away_team)

    # Reset team fouls for second half
    game_state["home_team_fouls"] = 0
    game_state["away_team_fouls"] = 0
    # Momentum decays at half -- fresh start
    home_momentum *= 0.4
    away_momentum *= 0.4

    # --- SECOND HALF ---
    home_score, away_score, home_momentum, away_momentum, game_state = simulate_period(
        home_lineup, away_lineup, home_bench, away_bench,
        half_possessions + remainder, home_score, away_score,
        home_momentum, away_momentum,
        home_profile, away_profile, home_coach, away_coach,
        fatigue_base=0, game_state=game_state,
        home_team=home_team, away_team=away_team,
        half_number=2)

    ot_periods = 0
    while home_score == away_score:
        ot_periods += 1
        if ot_periods > 4:
            home_score += 1
            break
        if verbose:
            print("  -- Overtime " + str(ot_periods) + " --")
        # OT: team fouls reset each OT period
        game_state["home_team_fouls"] = 0
        game_state["away_team_fouls"] = 0
        home_score, away_score, home_momentum, away_momentum, game_state = simulate_period(
            home_lineup, away_lineup, home_bench, away_bench,
            6, home_score, away_score, home_momentum, away_momentum,
            home_profile, away_profile, home_coach, away_coach,
            fatigue_base=0.7, game_state=game_state,
            home_team=home_team, away_team=away_team,
            half_number=2)

    ot_label = ""
    if   ot_periods == 1: ot_label = " (OT)"
    elif ot_periods > 1:  ot_label = " (" + str(ot_periods) + "OT)"

    if verbose:
        print("Final" + ot_label + ": " + home_name + " " +
              str(home_score) + ", " + away_name + " " + str(away_score))
        print_box_score(home_team)
        print_box_score(away_team)

    commit_game_to_season(home_team)
    commit_game_to_season(away_team)

    return {
        "home": home_score, "away": away_score, "ot": ot_periods,
        "home_name": home_name, "away_name": away_name,
        "possessions": possessions,
    }


def _simulate_game_legacy(home_team, away_team, possessions=None, verbose=False):
    """Fallback for simple rating dicts without rosters."""
    home_ratings = get_team_ratings(home_team) if "roster" in home_team else home_team
    away_ratings = get_team_ratings(away_team) if "roster" in away_team else away_team
    home_profile  = build_game_profile(home_team)
    away_profile  = build_game_profile(away_team)

    if possessions is None:
        pace_adj    = (home_profile["possessions_modifier"] +
                       away_profile["possessions_modifier"]) // 2
        possessions = max(55, min(82, 68 + pace_adj))

    home_score = away_score = 0
    for _ in range(possessions):
        for ratings, profile, side in [
            (home_ratings, home_profile, "home"),
            (away_ratings, away_profile, "away")
        ]:
            opp = away_ratings if side == "home" else home_ratings
            if random.random() >= profile["base_to_rate"]:
                prob = max(0.05, min(0.92,
                    0.38 + (ratings.get("shooting", 500) / 1000) * 0.25
                         - (opp.get("defense",  500) / 1000) * 0.14))
                if random.random() < prob:
                    pts = 3 if random.random() < 0.33 else 2
                    if side == "home": home_score += pts
                    else:              away_score += pts

    if home_score == away_score:
        home_score += 1

    return {
        "home": home_score, "away": away_score, "ot": 0,
        "home_name": home_ratings.get("name", "Home"),
        "away_name": away_ratings.get("name", "Away"),
        "possessions": possessions,
    }


