def resolve_rebound(offense_lineup, defense_lineup,
                    off_rebound_bonus=0.0, rebounding_mod=0.0):
    """
    Resolves who gets the rebound.
    Uses reb_window formula from C6.
    Returns (type, player_dict).
    """
    def reb_window(p):
        vt = p.get("vertical",   400)
        h  = p.get("height",      74)
        ws = p.get("wingspan",    74)
        st = p.get("strength",   400)
        pos_bonus = {"C": 150, "PF": 100, "SF": 50, "SG": 0, "PG": -50}
        pb = pos_bonus.get(p.get("position", "SF"), 0)
        fs = _frame_strength(p)
        return max(50, vt + h * 8 + ws * 4 + pb + fs * 0.3 * 100)

    avg_off_reb = (sum(reb_window(p) for p in offense_lineup) /
                   max(1, len(offense_lineup))) if offense_lineup else 500
    avg_def_reb = (sum(reb_window(p) for p in defense_lineup) /
                   max(1, len(defense_lineup))) if defense_lineup else 500

    # Defense has natural 1.2x advantage -- they're in position
    off_chance = (avg_off_reb / (avg_off_reb + avg_def_reb * 1.2))
    off_chance += off_rebound_bonus + rebounding_mod
    off_chance  = max(0.05, min(0.50, off_chance))

    if random.random() < off_chance:
        return "offensive", _select_rebounder(offense_lineup, True)
    else:
        return "defensive", _select_rebounder(defense_lineup, False)


