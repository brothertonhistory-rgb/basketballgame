def _determine_possession_state(offense_lineup, defense_lineup, coach_profile):
    """
    Determines how the offense enters this possession.
    Returns one of: half_court, drive, post_up, ball_movement, transition

    Coach philosophy modifies probabilities.
    """
    # Base weights
    state_weights = {
        "half_court":    35,
        "drive":         22,
        "post_up":       15,
        "ball_movement": 20,
        "transition":     8,
    }

    # Coach modifiers
    shot_profile  = coach_profile.get("shot_profile", 50)    # high = perimeter
    personnel     = coach_profile.get("personnel", 50)        # high = guard-heavy
    ball_movement = coach_profile.get("ball_movement", 50)    # high = motion
    inside_out    = coach_profile.get("inside_out_outside_in", 50)  # 1=post, 100=perimeter

    # Post-heavy system
    if inside_out < 40:
        state_weights["post_up"]   += int((40 - inside_out) * 0.4)
        state_weights["half_court"] -= int((40 - inside_out) * 0.2)

    # Motion / ball movement system
    if ball_movement > 55:
        state_weights["ball_movement"] += int((ball_movement - 55) * 0.5)
        state_weights["half_court"]    -= int((ball_movement - 55) * 0.3)

    # Guard-heavy / pace system
    if personnel > 60:
        state_weights["drive"]      += int((personnel - 60) * 0.3)
        state_weights["transition"] += int((personnel - 60) * 0.15)

    # Clamp to positive
    for k in state_weights:
        state_weights[k] = max(2, state_weights[k])

    states  = list(state_weights.keys())
    weights = list(state_weights.values())
    return random.choices(states, weights=weights, k=1)[0]


