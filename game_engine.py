def _remove_fouled_out(lineup, bench):
    """
    Remove players with 5+ fouls OR ejected from lineup.
    Replace with bench players if available.
    foul_count >= 5: fouled out
    foul_count >= 99: ejected (set by _eject_player)
    Returns adjusted lineup.
    """
    active  = []
    bench_q = [p for p in bench if not p.get("ejected", False)
               and p.get("foul_count", 0) < 5]

    for player in lineup:
        if player.get("ejected", False) or player.get("foul_count", 0) >= 5:
            if bench_q:
                replacement = bench_q.pop(0)
                active.append(replacement)
            # If no bench available, play short
        else:
            active.append(player)

    return active


