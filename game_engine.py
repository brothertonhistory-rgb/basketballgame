# DRIVE / ADVANTAGE MODEL (C3)
# -----------------------------------------

def _resolve_drive(ball_handler, primary_defender, offense_lineup,
                   defense_lineup, rim_protector=None):
    """
    Resolves whether a drive creates an advantage.
    Returns dict with:
      success: bool
      kick_out: bool
      kick_out_target: player or None
      preferred_bucket: "at_rim", "floater", or None
    """
    ex  = ball_handler.get("explosiveness",    400)
    sp  = ball_handler.get("speed",            400)
    bh  = ball_handler.get("ball_handling",    400)
    lq  = primary_defender.get("lateral_quickness", 400)
    dex = primary_defender.get("explosiveness",      400)
    obd = primary_defender.get("on_ball_defense",    400)

    drive_score = (ex * 0.40 + sp * 0.20 + bh * 0.15
                   - lq * 0.35 - dex * 0.15 - obd * 0.10)

    # Normalize: 500 = neutral
    drive_success_prob = 0.35 + (drive_score - 250) / 2000.0
    drive_success_prob = max(0.10, min(0.75, drive_success_prob))

    if not random.random() < drive_success_prob:
        return {"success": False, "kick_out": False,
                "kick_out_target": None, "preferred_bucket": None}

    # Drive succeeded -- is there a rim protector?
    if rim_protector:
        rim_block  = rim_protector.get("shot_blocking", 200)
        rim_height = rim_protector.get("height", 74)

        # Rim protection score -- high means driver should kick out
        rim_threat = rim_block / 1000.0 * 0.6 + (rim_height - 74) / 20.0 * 0.4

        # Kick-out decision
        cv   = ball_handler.get("court_vision",    400)
        ps   = ball_handler.get("passing",         400)
        iq   = ball_handler.get("basketball_iq",   10)
        kick_chance = (cv / 1000.0 * 0.5 + ps / 1000.0 * 0.3 +
                       iq / 20.0 * 0.2) * rim_threat * 1.5
        kick_chance = max(0.0, min(0.85, kick_chance))

        if random.random() < kick_chance:
            # Find best open shooter
            target = _select_best_shooter(offense_lineup, "corner_three")
            return {"success": True, "kick_out": True,
                    "kick_out_target": target, "preferred_bucket": None}

        # Attack the rim anyway -- agility determines floater vs at_rim
        agility = ball_handler.get("agility", 400)
        if agility > 600:
            preferred = "floater" if random.random() < 0.45 else "at_rim"
        else:
            preferred = "at_rim"
        return {"success": True, "kick_out": False,
                "kick_out_target": None, "preferred_bucket": preferred}

    # Clean drive -- go to rim
    return {"success": True, "kick_out": False,
            "kick_out_target": None, "preferred_bucket": "at_rim"}


def _find_rim_protector(defense_lineup):
    """
    Finds the primary rim protector in the defense.
    C and PF with high shot_blocking get priority.
    """
    candidates = [p for p in defense_lineup
                  if p.get("position") in ("C", "PF")]
    if not candidates:
        return None
    return max(candidates, key=lambda p: p.get("shot_blocking", 200))


