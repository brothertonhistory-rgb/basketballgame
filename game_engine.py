# SHOT RESOLUTION (Phase B)
# -----------------------------------------

def resolve_shot_bucket(shooter, defender, bucket,
                        possession_state="half_court",
                        is_catch_situation=False,
                        fatigue=0, momentum=0,
                        burst_modifier=0.0,
                        defense_lineup=None,
                        position_established=False):
    """
    Resolves a shot from a specific bucket.
    Returns (result, points, is_three, ft_made, ft_att, block_by, block_outcome)
    result: "make", "miss", "foul", "miss_airball", "make_and1"

    position_established: True when the offense has clear body position advantage --
      post catch with the defender on their back, cutter at the rim on a dump-off,
      roll man receiving a pass at the rim. Contest is reduced to 20-30% of normal.
      This models the easy baskets that happen every game regardless of opponent quality.
    """
    bucket_def   = SHOT_BUCKETS[bucket]
    primary_attr = bucket_def["primary_attr"]
    defense_attr = bucket_def["defense_attr"]
    is_three     = bucket_def["is_three"]
    base_make    = bucket_def["base_make"]
    skill_range  = bucket_def["skill_range"]
    foul_base    = bucket_def["foul_base"]

    # --- MAKE PROBABILITY ---
    shoot_val = shooter.get(primary_attr, 400)
    # Skill modifies make rate: 0 skill = base - range, 1000 skill = base + range
    make_prob = base_make + (shoot_val / 1000.0 - 0.4) * skill_range * 2.5
    make_prob = max(0.05, min(0.88, make_prob))

    # catch_and_shoot bonus on three buckets (catch situation only)
    if is_three and is_catch_situation:
        cas = shooter.get("catch_and_shoot", 400)
        cas_bonus = (cas / 1000.0) * CATCH_AND_SHOOT_MAX_BONUS
        make_prob += cas_bonus

    # --- DEFENSE CONTEST ---
    if defender:
        def_val = defender.get(defense_attr, 400)

        if bucket in ("at_rim", "floater"):
            # Length rating affects rim contests
            lr           = _length_rating(defender)
            length_bonus = max(0, lr) * 0.00015
            contest      = (def_val / 1000.0) * 0.12 + length_bonus
        elif bucket == "post":
            # Frame strength battle
            off_fs  = _frame_strength(shooter)
            def_fs  = _frame_strength(defender)
            fs_diff = off_fs - def_fs
            contest = (def_val / 1000.0) * 0.10 - fs_diff * 0.06
            contest = max(0, contest)
        elif bucket in ("corner_three", "above_break_three"):
            # Perimeter contest: lateral quickness only on catch situations
            contest = (def_val / 1000.0) * 0.10
            if not is_catch_situation:
                ath     = _athleticism_composite(defender)
                contest += (ath / 1000.0) * 0.03
        else:
            contest = (def_val / 1000.0) * 0.10

        # --- POSITION ESTABLISHED ---
        # Defender is late or out of position. Contest reduced to 25% of normal.
        # Models: clean post catch, roll man at rim, cutter on dump-off.
        # Even elite defenders can't fully contest what they're late to.
        if position_established and bucket in ("at_rim", "post", "putback"):
            contest *= 0.25

        make_prob -= contest
        make_prob  = max(0.05, make_prob)

    # --- EXPERIENCE MODIFIER (A9) ---
    year = shooter.get("year", "Sophomore")
    exp_mod = {"Freshman": -0.03, "Sophomore": -0.01,
               "Junior": 0.01, "Senior": 0.02}.get(year, 0)
    make_prob += exp_mod

    # Youth variance -- freshmen have wider outcome distribution
    if year == "Freshman":
        variance = random.gauss(0, 0.04)
        make_prob += variance

    # Fatigue penalty
    endurance    = shooter.get("endurance", 500)
    fatigue_mult = max(0.5, endurance / 500.0)
    make_prob   -= fatigue * 0.020 * (1.0 / fatigue_mult)

    # Momentum
    make_prob += momentum * 0.025

    # Burst modifier (changes decision quality, not make rate directly)
    make_prob += burst_modifier * 0.03

    # IQ bonus
    iq         = shooter.get("basketball_iq", 10)
    make_prob += (iq - 10) / 10.0 * 0.015

    make_prob = max(0.04, min(0.90, make_prob))

    # --- FOUL CHECK ---
    # Foul probability is context-specific and independent of make/miss
    foul_prob = _calculate_foul_probability(
        shooter, defender, bucket, possession_state, fatigue
    )

    if random.random() < foul_prob:
        return "foul", 0, is_three, 0, 0, None, None

    # --- MAKE / MISS ---
    if random.random() < make_prob:
        pts = 3 if is_three else 2
        # AND-1 check: foul on the make (at_rim ~8%, post ~5%, floater ~3%)
        and1_rates = {"at_rim": 0.08, "post": 0.05, "floater": 0.03,
                      "putback": 0.06}
        and1_chance = and1_rates.get(bucket, 0.0)
        if and1_chance > 0 and defender and random.random() < and1_chance:
            # Fouled on the make -- 1 FT, defender gets foul charged
            # Return "make_and1" so simulate_possession handles the FT
            return "make_and1", pts, is_three, 0, 0, None, None
        return "make", pts, is_three, 0, 0, None, None
    else:
        # Air ball check -- no rebound contest, defense gets possession
        # Air balls are clean misses with no chance of offensive rebound
        # Probability: low for interior, higher for long threes
        air_ball_chance = {
            "at_rim":            0.01,
            "floater":           0.02,
            "post":              0.02,
            "mid_two":           0.04,
            "corner_three":      0.05,
            "above_break_three": 0.07,
            "putback":           0.01,
        }.get(bucket, 0.03)
        if random.random() < air_ball_chance:
            return "miss_airball", 0, is_three, 0, 0, None, None

        # Block check on interior shots
        # Use the best available shot blocker in the defense, not just
        # the matched defender. On drives and post-ups, the rim protector
        # rotates to contest -- that's the whole point of having a shot blocker.
        block_by = None
        block_outcome = None   # "defense", "oob_offense", "offense_recovery"
        if bucket in ("at_rim", "floater", "post") and defense_lineup:
            # Find best shot blocker available (the rim protector)
            rim_prot = max(defense_lineup,
                           key=lambda p: p.get("shot_blocking", 200) * 0.7
                                        + p.get("vertical", 400) * 0.3)
            block_chance = _calculate_block_probability(rim_prot, shooter, bucket)
            if random.random() < block_chance:
                block_by = rim_prot
                # Resolve where the blocked ball goes
                # ~60% defense recovers, ~20% off blocker OOB (offense ball), ~20% offense scramble
                r = random.random()
                if r < 0.60:
                    block_outcome = "defense"       # normal -- defense gets it
                elif r < 0.80:
                    block_outcome = "oob_offense"   # ball off blocker OOB, offense inbounds
                else:
                    block_outcome = "offense_recovery"  # scramble, offense keeps it

        return "miss", 0, is_three, 0, 0, block_by, block_outcome


def _calculate_foul_probability(shooter, defender, bucket,
                                  possession_state, fatigue):
    """
    Foul probability is shot-context dependent (C7).

    at_rim drive: blocking foul based on defender agility/positioning
    post:         reach-in from strength disparity
    perimeter:    hand-check from length/aggression
    """
    base = SHOT_BUCKETS[bucket]["foul_base"]

    if not defender:
        return base * 0.3

    foul_count = defender.get("foul_count", 0)
    # Players in foul trouble play more carefully
    foul_trouble_mod = max(0.4, 1.0 - foul_count * 0.12)

    if bucket == "at_rim":
        # Blocking foul: defender agility/positioning vs shooter agility
        def_agility  = defender.get("agility",       400)
        shoot_agility = shooter.get("agility",        400)
        shoot_fin    = shooter.get("finishing",       400)
        # Agile shooters draw fouls; slow defenders give them
        foul_mod = ((shoot_agility - def_agility) / 1000.0 * 0.12
                    + (shoot_fin / 1000.0) * 0.06)
        foul_prob = (base + foul_mod) * foul_trouble_mod

    elif bucket == "post":
        # Reach-in: strength disparity
        off_fs    = _frame_strength(shooter)
        def_fs    = _frame_strength(defender)
        fs_diff   = max(0, off_fs - def_fs)
        foul_prob = (base + fs_diff * 0.04) * foul_trouble_mod

    elif bucket in ("corner_three", "above_break_three"):
        # Hand-check: length + aggression
        lr        = max(0, _length_rating(defender))
        foul_tend = defender.get("foul_tendency", 400)
        foul_prob = (base + (foul_tend / 1000.0) * 0.03
                     - lr * 0.00005) * foul_trouble_mod

    elif bucket == "floater":
        # Shot blocker flailing
        blk_val   = defender.get("shot_blocking", 200)
        foul_prob = (base + (blk_val / 1000.0) * 0.05) * foul_trouble_mod

    else:
        foul_prob = base * foul_trouble_mod

    # Fatigue increases foul rate (tired defenders lose positioning)
    foul_prob += fatigue * 0.02

    return max(0.01, min(0.45, foul_prob))


def _calculate_block_probability(defender, shooter, bucket):
    """
    Block probability by bucket type.
    Uses shot_blocking, length, and vertical vs shooter agility.
    """
    blk = defender.get("shot_blocking", 200)
    lr  = max(0, _length_rating(defender))
    vt  = defender.get("vertical",     400)

    # Shooter agility reduces block chance
    shoot_agility = shooter.get("agility", 400)

    if bucket == "at_rim":
        prob = ((blk / 1000.0) * 0.42 + (lr * 0.0006)
                + (vt / 1000.0) * 0.14 - (shoot_agility / 1000.0) * 0.06)
    elif bucket == "floater":
        # Floater designed to get over length -- elite blockers still contest
        prob = ((blk / 1000.0) * 0.26 + (vt / 1000.0) * 0.08
                - (shoot_agility / 1000.0) * 0.05)
    elif bucket == "post":
        prob = ((blk / 1000.0) * 0.18 + (lr * 0.0004))
    else:
        prob = 0.0

    return max(0.0, min(0.35, prob))


