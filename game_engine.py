def run_calibration_check(n_games=200, verbose=False):
    """
    Runs n_games and reports key calibration stats vs targets.
    Call this after any engine change to verify targets are met.
    """
    from programs_data import build_all_d1_programs
    from roster_minutes import allocate_minutes
    from cohesion import update_cohesion

    print("Loading programs for calibration...")
    all_programs = build_all_d1_programs()
    for program in all_programs:
        allocate_minutes(program)
        update_cohesion(program, previous_minutes=None)
        initialize_season_stats(program, season_year=2024)

    import random as rnd
    game_programs = [p for p in all_programs if p.get("roster")]
    rnd.shuffle(game_programs)

    totals = {
        "pts": 0, "fg_att": 0, "fg_made": 0,
        "three_att": 0, "three_made": 0,
        "ft_att": 0, "ft_made": 0,
        "to": 0, "blk": 0, "ast": 0, "fouls": 0,
        "two_att": 0,
        "games": 0,
    }

    for i in range(min(n_games, len(game_programs) // 2)):
        home = game_programs[i * 2]
        away = game_programs[i * 2 + 1]

        for p in home.get("roster", []):
            p["foul_count"] = 0
            p["game_stats"] = _empty_game_stats()
        for p in away.get("roster", []):
            p["foul_count"] = 0
            p["game_stats"] = _empty_game_stats()

        result = simulate_game(home, away, verbose=False, season_year=2024)

        # commit_game_to_season clears game_stats -- read from season_stats instead
        # Each game adds 1 to season_stats["games"] per player. We want just this game's
        # contribution, so we snapshot season_stats before and diff -- but simpler:
        # just read the score totals from the result and accumulate box score stats
        # by re-reading season_stats and computing the per-game contribution.
        # Simplest correct approach: read season_stats game count and use per-game avgs.
        for team in [home, away]:
            ss = team.get("season_stats", {})
            for name, stats in ss.items():
                games_played = stats.get("games", 0)
                if games_played < 1:
                    continue
                # Each game in calibration is the ONLY game these players have played
                # (fresh season_stats initialized above), so season total == this game
                totals["pts"]       += stats.get("points",    0)
                totals["fg_att"]    += stats.get("fg_att",    0)
                totals["fg_made"]   += stats.get("fg_made",   0)
                totals["three_att"] += stats.get("three_att", 0)
                totals["three_made"]+= stats.get("three_made",0)
                totals["ft_att"]    += stats.get("ft_att",    0)
                totals["ft_made"]   += stats.get("ft_made",   0)
                totals["to"]        += stats.get("turnovers", 0)
                totals["blk"]       += stats.get("blocks",    0)
                totals["ast"]       += stats.get("assists",   0)
                totals["fouls"]     += stats.get("fouls",     0)

        totals["games"] += 2  # 2 team-games per matchup

    g = max(1, totals["games"])
    fg  = max(1, totals["fg_att"])
    fg2 = max(1, totals["fg_att"] - totals["three_att"])

    ppg          = round(totals["pts"]       / g,  1)
    three_rate   = round(totals["three_att"] / fg * 100, 1)
    fta_fga      = round(totals["ft_att"]    / fg * 100, 1)
    to_per_game  = round(totals["to"]        / g,  1)
    blk_pct      = round(totals["blk"]       / max(1, fg2) * 100, 1)
    ast_fgm      = round(totals["ast"]       / max(1, totals["fg_made"]) * 100, 1)
    fouls_per_g  = round(totals["fouls"]     / g,  1)

    print("")
    print("=" * 60)
    print("  CALIBRATION CHECK -- " + str(n_games) + " games")
    print("=" * 60)
    print("{:<28} {:>8} {:>12}".format("Stat", "Actual", "Target"))
    print("-" * 50)

    checks = [
        ("PPG (per team)",      ppg,         "68-72"),
        ("3PA/FGA %",           three_rate,  "33-36%"),
        ("FTA/FGA %",           fta_fga,     "38-42%"),
        ("TO per team per game", to_per_game, "13-15"),
        ("Block % of 2PA",      blk_pct,     "8-10%"),
        ("AST/FGM %",           ast_fgm,     "52-56%"),
        ("Fouls per team/game", fouls_per_g, "18-22"),
    ]

    for label, actual, target in checks:
        print("{:<28} {:>8} {:>12}".format(label, str(actual), target))

    print("=" * 60)
    return totals


