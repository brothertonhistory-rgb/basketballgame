def _select_shooter_for_bucket(lineup, bucket, possession_state):
    """
    Select which player takes this shot bucket.
    Uses primary attribute skill + position fit.
    """
    if not lineup:
        return lineup[0] if lineup else None

    bucket_def  = SHOT_BUCKETS[bucket]
    primary     = bucket_def["primary_attr"]

    # Position tendencies per bucket
    pos_weights = {
        "at_rim":            {"PG": 12, "SG": 14, "SF": 18, "PF": 24, "C": 24},
        "floater":           {"PG": 32, "SG": 26, "SF": 18, "PF": 14, "C":  8},
        "post":              {"PG":  4, "SG":  5, "SF": 15, "PF": 36, "C": 40},
        "mid_two":           {"PG": 20, "SG": 26, "SF": 24, "PF": 18, "C": 12},
        "corner_three":      {"PG": 14, "SG": 30, "SF": 28, "PF": 18, "C": 10},
        "above_break_three": {"PG": 28, "SG": 28, "SF": 22, "PF": 14, "C":  8},
        "putback":           {"PG":  8, "SG": 10, "SF": 18, "PF": 28, "C": 36},
    }

    pw = pos_weights.get(bucket, {})

    scores = []
    for player in lineup:
        pos       = player.get("position", "SF")
        pos_w     = pw.get(pos, 12)
        skill_val = player.get(primary, 400)
        # Skill amplifies position weight
        scores.append(pos_w * (skill_val / 500.0))

    total = sum(scores)
    if total <= 0:
        return random.choice(lineup)
    return random.choices(lineup, weights=scores, k=1)[0]


def _select_defender(shooter, defending_lineup):
    if not defending_lineup:
        return defending_lineup[0] if defending_lineup else None

    shooter_pos   = shooter.get("position", "SF")
    matchup_order = POSITION_MATCHUP_ORDER.get(shooter_pos, ["SF", "SG", "PF"])

    for target_pos in matchup_order:
        for player in defending_lineup:
            if player.get("position") == target_pos:
                return player

    return random.choice(defending_lineup)


def _select_rebounder(lineup, is_offensive):
    if not lineup:
        return None

    pos_bonus = {"C": 1.3, "PF": 1.2, "SF": 1.0, "SG": 0.85, "PG": 0.75}
    scores    = [
        player.get("rebounding", 400) * pos_bonus.get(player.get("position", "SF"), 1.0)
        for player in lineup
    ]
    total = sum(scores)
    if total <= 0:
        return random.choice(lineup)
    return random.choices(lineup, weights=scores, k=1)[0]


def _select_steal_player(defending_lineup, steal_type="bad_pass"):
    """
    Steal attribution depends on turnover type.
    bad_pass + intercepted: steal_tendency + length
    ball_handling: steal_tendency + lateral_quickness
    """
    if not defending_lineup:
        return None

    scores = []
    for p in defending_lineup:
        st = max(1, p.get("steal_tendency", 300))
        if steal_type == "intercepted":
            scores.append(st * 1.5)
        elif steal_type == "ball_handling":
            lq = p.get("lateral_quickness", 400)
            scores.append((st + lq) / 2)
        else:  # bad_pass
            lr = max(0, _length_rating(p))
            scores.append(st + lr * 0.05)

    total = sum(scores)
    if total <= 0:
        return random.choice(defending_lineup)
    return random.choices(defending_lineup, weights=scores, k=1)[0]


def _select_block_player(defending_lineup):
    if not defending_lineup:
        return None
    scores = [max(1, p.get("shot_blocking", 200)) for p in defending_lineup]
    return random.choices(defending_lineup, weights=scores, k=1)[0]


def _select_best_shooter(lineup, bucket):
    """Find the best available shooter for a kick-out opportunity."""
    if not lineup:
        return None
    primary = SHOT_BUCKETS.get(bucket, {}).get("primary_attr", "three_point")
    return max(lineup, key=lambda p: p.get(primary, 400))


