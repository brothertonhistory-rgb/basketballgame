def _resolve_free_throws(shooter, n_shots, burst_modifier=0.0):
    """
    Resolves n free throw attempts.
    Returns (made, attempted).
    """
    ft_attr = shooter.get("free_throw", 400)
    # FT% formula: base 0.40 + skill contribution up to 0.55
    ft_pct  = 0.40 + (ft_attr / 1000.0) * 0.55
    ft_pct += burst_modifier * 0.02

    # Experience modifier
    year    = shooter.get("year", "Sophomore")
    ft_pct += {"Freshman": -0.03, "Sophomore": -0.01,
               "Junior": 0.01, "Senior": 0.02}.get(year, 0)

    ft_pct  = max(0.35, min(0.98, ft_pct))
    made    = sum(1 for _ in range(n_shots) if random.random() < ft_pct)
    return made, n_shots


def _resolve_free_throws_one_and_one(shooter, burst_modifier=0.0):
    """
    Resolves a 1-and-1 free throw situation.
    Make the first = attempt the second.
    Miss the first = done.
    Returns (made, attempted).
    """
    ft_attr = shooter.get("free_throw", 400)
    ft_pct  = 0.40 + (ft_attr / 1000.0) * 0.55
    ft_pct += burst_modifier * 0.02
    year    = shooter.get("year", "Sophomore")
    ft_pct += {"Freshman": -0.03, "Sophomore": -0.01,
               "Junior": 0.01, "Senior": 0.02}.get(year, 0)
    ft_pct  = max(0.35, min(0.98, ft_pct))

    # First attempt
    if random.random() < ft_pct:
        # Made first -- attempt second
        second = 1 if random.random() < ft_pct else 0
        return (1 + second, 2)
    else:
        # Missed first -- done
        return (0, 1)


