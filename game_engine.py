def _check_assist(possession_state, shooter, offense_lineup,
                   is_catch_situation=False):
    """
    Returns (assist_player, assist_bool) based on possession state.
    No play-by-play required.

    ball_movement + kick_outs generate assists naturally.
    Self-created shots don't.
    """
    if possession_state == "ball_movement":
        # Motion/ball movement -- credit a passer (not the shooter)
        # Ball movement almost always generates an assist
        passers = [p for p in offense_lineup if p is not shooter]
        if not passers:
            return None, False
        scores = [p.get("court_vision", 400) * 0.5 +
                  p.get("passing",      400) * 0.5
                  for p in passers]
        passer = random.choices(passers, weights=scores, k=1)[0]
        return passer, True

    elif possession_state == "transition":
        # Transition -- outlet pass or lead pass almost always an assist
        passers = [p for p in offense_lineup if p is not shooter]
        if not passers:
            return None, False
        if random.random() < 0.82:   # transition assists ~82% of transition makes
            scores = [p.get("speed", 400) * 0.4 +
                      p.get("passing", 400) * 0.6
                      for p in passers]
            passer = random.choices(passers, weights=scores, k=1)[0]
            return passer, True
        return None, False

    elif possession_state == "drive" and is_catch_situation:
        # Kick-out from drive -- credit the driver (different player)
        drivers = [p for p in offense_lineup if p is not shooter]
        if not drivers:
            return None, False
        scores = [p.get("court_vision", 400) * 0.5 +
                  p.get("passing",      400) * 0.3 +
                  p.get("explosiveness",400) * 0.2
                  for p in drivers]
        driver = random.choices(drivers, weights=scores, k=1)[0]
        return driver, True

    elif possession_state == "post_up" and is_catch_situation:
        # Post kick-out
        distributors = [p for p in offense_lineup if p is not shooter]
        if not distributors:
            return None, False
        scores = [p.get("passing", 400) * 0.6 +
                  p.get("court_vision", 400) * 0.4
                  for p in distributors]
        distributor = random.choices(distributors, weights=scores, k=1)[0]
        return distributor, True

    elif possession_state == "half_court":
        # Set play -- probabilistic assist check
        cv = max(p.get("court_vision", 400) for p in offense_lineup)
        ps = max(p.get("passing",      400) for p in offense_lineup)
        ast_prob = 0.58 + (cv / 1000.0) * 0.18 + (ps / 1000.0) * 0.12
        ast_prob = min(0.88, ast_prob)
        if random.random() < ast_prob:
            passers = [p for p in offense_lineup if p is not shooter]
            if passers:
                scores = [p.get("passing", 400) for p in passers]
                return random.choices(passers, weights=scores, k=1)[0], True

    return None, False


